/**
 * @packageDocumentation
 * Generate rule reference pages from the built plugin's public metadata.
 */
// @ts-check

import { readFile, writeFile } from "node:fs/promises";
import { resolve } from "node:path";

import { format, resolveConfig } from "prettier";

import plugin from "../dist/plugin.js";

const docsDirectory = resolve(process.cwd(), "docs", "rules");
const shouldWrite = process.argv.includes("--write");
const shouldCheck = process.argv.includes("--check");

/**
 * @param {unknown} value
 *
 * @returns {value is Readonly<Record<string, unknown>>}
 */
const isRecord = (value) =>
    typeof value === "object" && value !== null && !Array.isArray(value);

/** @param {string} value */
const sentenceCase = (value) =>
    value.length === 0 ? value : value.charAt(0).toUpperCase() + value.slice(1);

/** @param {string} value */
const escapeMarkdownText = (value) =>
    value.replaceAll("[", "\\[").replaceAll("]", "\\]");

/** @param {string} ruleName */
const sourceForRule = (ruleName) => {
    if (ruleName.includes("skill")) {
        return ["Agent Skills", "https://learn.chatgpt.com/docs/build-skills"];
    }

    if (
        ruleName.includes("agents-instruction") ||
        ruleName.includes("agents-md")
    ) {
        return [
            "Custom instructions with AGENTS.md",
            "https://learn.chatgpt.com/docs/agent-configuration/agents-md",
        ];
    }

    if (ruleName.includes("hook")) {
        return ["Codex hooks", "https://learn.chatgpt.com/docs/hooks"];
    }

    if (ruleName.includes("mcp")) {
        return [
            "Codex MCP configuration",
            "https://learn.chatgpt.com/docs/extend/mcp",
        ];
    }

    if (ruleName.includes("agent")) {
        return [
            "Codex subagents",
            "https://learn.chatgpt.com/docs/agent-configuration/subagents",
        ];
    }

    if (ruleName.includes("prompt")) {
        return [
            "Codex custom prompts",
            "https://learn.chatgpt.com/docs/custom-prompts",
        ];
    }

    return [
        "Codex configuration reference",
        "https://learn.chatgpt.com/docs/config-file/config-reference",
    ];
};

/** @param {string} ruleName */
const scopesForRule = (ruleName) => {
    if (
        ruleName.includes("agents-instruction") ||
        ruleName.includes("agents-md")
    ) {
        return ["`**/AGENTS.md`", "`**/AGENTS.override.md`"];
    }

    if (ruleName.includes("skill")) {
        return [
            "`**/.agents/skills/**/*.md`",
            "Codex plugin skill directories",
        ];
    }

    if (
        ruleName.includes("hook") &&
        ruleName !== "no-mixed-hook-representations"
    ) {
        return [
            "`**/.codex/hooks.json`",
            "inline `[hooks]` tables in `**/.codex/config.toml`",
        ];
    }

    if (ruleName.includes("agent")) {
        return ["`**/.codex/agents/*.toml`"];
    }

    if (ruleName.includes("prompt")) {
        return ["`**/.codex/prompts/*.md`"];
    }

    return [
        "`**/.codex/config.toml`",
        "standalone `*.config.toml` profiles where applicable",
    ];
};

/** @param {string} ruleName @param {Readonly<Record<string, unknown>>} rule */
const renderRulePage = (ruleName, rule) => {
    const rawMeta = rule["meta"];
    const meta = isRecord(rawMeta) ? rawMeta : {};
    const rawDocs = meta["docs"];
    const rawMessages = meta["messages"];
    const docs = isRecord(rawDocs) ? rawDocs : {};
    const messages = isRecord(rawMessages) ? rawMessages : {};
    const description =
        typeof docs["description"] === "string" ? docs["description"] : "";
    const ruleId = typeof docs["ruleId"] === "string" ? docs["ruleId"] : "";
    const configNames = Array.isArray(docs["codexConfigNames"])
        ? docs["codexConfigNames"].filter(
              (/** @type {unknown} */ value) => typeof value === "string"
          )
        : [];
    const [sourceLabel, sourceUrl] = sourceForRule(ruleName);
    const optionLines =
        ruleName === "max-agents-instruction-chain-bytes"
            ? [
                  "The optional object accepts `maxBytes` (default `32768`) and `fallbackFilenames`, an array of additional basename-only instruction files. The byte count uses UTF-8 and follows Codex's active instruction-file precedence.",
                  "",
              ]
            : [];

    return [
        "<!-- This file is generated by scripts/generate-rule-docs.mjs. -->",
        `# ${ruleName}`,
        "",
        sentenceCase(description),
        "",
        `> **Rule catalog ID:** ${ruleId}`,
        "",
        "## Targeted pattern scope",
        "",
        ...scopesForRule(ruleName).map((scope) => `- ${scope}`),
        "",
        "## What this rule reports",
        "",
        ...Object.values(messages).map(
            (message) => `- ${escapeMarkdownText(String(message))}`
        ),
        "",
        "## Why this rule exists",
        "",
        sentenceCase(description),
        "",
        "## ❌ Incorrect",
        "",
        "A file in the targeted scope that produces any diagnostic listed above is incorrect for this rule.",
        "",
        "## ✅ Correct",
        "",
        "A file is correct when it uses the documented Codex structure and produces no diagnostic from this rule.",
        "",
        "## Behavior and migration notes",
        "",
        "Included in:",
        "",
        ...configNames.map((name) => `- \`codex.configs.${name}\``),
        "",
        ...optionLines,
        "## ESLint flat config example",
        "",
        "The preset layers supply the correct Markdown, TOML, or JSON language configuration. Override the rule only in a later matching flat-config entry:",
        "",
        "```js",
        'import codex from "eslint-plugin-codex";',
        "",
        "export default [",
        " ...codex.configs.recommended,",
        " {",
        `  rules: { "codex/${ruleName}": "error" },`,
        " },",
        "];",
        "```",
        "",
        "## Further reading",
        "",
        `- [${sourceLabel}](${sourceUrl})`,
        "- [Agent Skills specification](https://agentskills.io/specification)",
        "",
    ].join("\n");
};

let changed = false;

for (const ruleName of Object.keys(plugin.rules).toSorted()) {
    const rule = plugin.rules[ruleName];

    if (!isRecord(rule)) {
        throw new TypeError(`Rule '${ruleName}' is not an object.`);
    }

    const filePath = resolve(docsDirectory, `${ruleName}.md`);
    const generated = await format(renderRulePage(ruleName, rule), {
        ...(await resolveConfig(filePath)),
        filepath: filePath,
    });
    let current = "";

    try {
        current = await readFile(filePath, "utf8");
    } catch (error) {
        if (
            !(error instanceof Error) ||
            !("code" in error) ||
            error.code !== "ENOENT"
        ) {
            throw error;
        }
    }

    if (current.replaceAll("\r\n", "\n") === generated) {
        continue;
    }

    changed = true;

    if (shouldWrite) {
        await writeFile(filePath, generated, "utf8");
    }
}

if (shouldCheck && changed) {
    throw new Error(
        "Rule reference pages are stale. Run `npm run sync:rule-docs:write`."
    );
}
